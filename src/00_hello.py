# Print "Hello, world!" to your terminal
print('Hello, world!')

name = "Bob"

# String interpolation using +
print("Hello " + name)

# String interpolation using fstring
print(f"Hello {name}")

# if statements
# in Python, whitespace is important
if name != "Sean":
    print("Hey, you aren't Sean")
else:
    print(f"Hello {name}")