# Write a function is_even that will return true if the passed in number is even.
def is_even(num):
    if num % 2 == 0:
        return True
    else:
        return False


# Read a number from the keyboard
num = input("Enter a number: ")

# Print out "Even!" if the number is even. Otherwise print "Odd"
if num == True:
    print("Even")
if num == False:
    print("Odd")

